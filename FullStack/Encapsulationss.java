package FullStack;

public class Encapsulationss {
	
	private String food;
	private int price;
	
	public String getFood() {
		return food;
		
		
	}
	
	public void setFood(String food) {
		this.food=food;
	}
	
	
	public int getPrice() {
		return price;
	}
	
	public void setPrice(int price) {
		this.price=price;
		
	}
	

}
