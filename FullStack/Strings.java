package FullStack;

public class Strings {
	public static void main (String args[]) {
		String car[]= {"BMW","AUDI","MAZDA","VOLVA"};
		System.out.println(car.length);
		
		for(String j:car) {
			System.out.println(j);
		}
		for(int i=0;i<car.length;i++) {
			System.out.println(car[i]);
		}
	}

}
